/*!
* \brief Função: ehPar
* Verifica se um número é par ou não.
* \param numero 1: (int) número que vai ter a paridade testada.
* \return retorno: (int) 1 se o número for par e 0 se não for par.
*/
int ehPar(long long numero);

/*!
* \brief Função: ehPrimo
* Verifica se um número é primo ou não.
* \param numero 1: (int) número que vai ser verificado se é primo.
* \return retorno: (int) 1 se o número for primo e 0 caso contrário.
*/
int ehPrimo(long long numero);

/*!
* \brief Função: ehQuadradoPerfeito
* Verifica se um número é quadrado perfeito ou não.
* \param numero 1: (int) número que vai ser testado se é um quadrado perfeito..
* \return retorno: (int) 1 se o número for um quadrado perfeito e 0 caso contrário.
*/
int ehQuadradoPerfeito(long long numero);

/*!
* \brief Função: ehCuboPerfeito
* Verifica se um número é um cubo perfeito ou não.
* \param numero 1: (int) número que vai ser testado se é um cubo perfeito.
* \return retorno: (int) 1 se o número for cubo perfeito e 0 caso contrário.
*/
int ehCuboPerfeito(long long numero);

/*!
* \brief Função: ehFibonacci
* Verifica se um número faz parte da sequência de Fibonacci ou não.
* \param numero 1: (int) número que vai testado para verificar se esta contido na sequência de FIbonacci.
* \return retorno: (int) 1 se o número fazer parte da sequência de Fibonacci e 0 caso contrário.
*/
int ehFibonacci(long long numero);

/*!
* \brief Função: ehFatorial
* Verifica se um número é um resultado de um numero fatorial.
* \param numero 1: (int) número que vai ser testado se é um fatorial.
* \return retorno: (int) 1 se o número for um fatorial e 0 caso contrário.
*/
int ehFatorial(long long numero);
